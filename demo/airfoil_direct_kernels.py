from pyop2.op2 import Kernel

save_soln_code = """
void save_soln(double *q, double *qold){
  for (int n=0; n<4; n++) qold[n] = q[n];
}
"""

dummy_code = """
void dummy(double *res, double *adt) {
    res[0] += gam*0.3;
    res[1] += gm1*0.8;
    res[2] += 1.0;
    res[3] += cfl*8.3;

    *adt += 5.63 * alpha;

    alpha += res[0];
    gam *= res[1];
}
"""

update_code = """
void update(double *qold, double *q, double *res, double *adt, double *rms){
  double del, adti;

  adti = 1.0f/(*adt);

  for (int n=0; n<4; n++) {
    del    = adti*res[n];
    q[n]   = qold[n] - del;
    res[n] = 0.0f;
    *rms  += del*del;
  }
}
"""

save_soln = Kernel(save_soln_code, "save_soln")
dummy = Kernel(dummy_code, "dummy")
update = Kernel(update_code, "update")
